CREATE OR REPLACE FUNCTION WORKING_CYCLE_DESC(
    P_WORKING_CYCLE CHAR)
  RETURN VARCHAR2
IS
  V_WORKING_CYCLE_DESC VARCHAR2(50 BYTE);
BEGIN
  V_WORKING_CYCLE_DESC:=
  (
    CASE P_WORKING_CYCLE
    WHEN 'W' THEN
      'Weekly'
    WHEN 'M'THEN
      'Monthly'
    WHEN 'R' THEN
      'Randomly'
    END);
  RETURN V_WORKING_CYCLE_DESC;
END; 